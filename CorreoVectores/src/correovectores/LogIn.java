    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean Validar=true;
        String usuario = jTextField1.getText();
        String Contrasena = new String(jPasswordField1.getPassword());
        for (int i = 0; i < cont; i++) {
            if (cola[i].getUsuario().equals(usuario) && cola[i].getPassword().equals(Contrasena)) {
                Usuario temp = cola[i];
                Inbox in = new Inbox(new javax.swing.JDialog(), true, cola,temp, cont);
                in.setVisible(true);
            } else {
                Validar=false;
            }
        }
        
        if (Validar==false) {
            JOptionPane.showMessageDialog(null, "Usuario o ContraseÃ±a incorrecto");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

